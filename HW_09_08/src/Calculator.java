public class Calculator {


    int add(int val, int val1) {
        return val + val1;

    }
    int subtract(int val, int val1) {
        return val - val1;
    }

    public int multi(int val, int val1) {

        return val * val1;
    }
    int divide(int val, int val1) {

        return val / val1;
    }

}